package Bean;

/**
 * 工数全般管理Beanクラス
 *
 */
public class MatterBean {
	private int code;
	private String name;
	
	public int getMcode() {
		return code;
	}
	public void setMcode(int mcode) {
		this.code = mcode;
	}
	public String getMname() {
		return name;
	}
	public void setMname(String mname) {
		this.name = mname;
	}
}
